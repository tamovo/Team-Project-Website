$(window).load(function() {
$('.portfolio_menu ul li').click(function(){
	$('.portfolio_menu ul li').removeClass('active_prot_menu');
	$(this).addClass('active_prot_menu');
});

var $container = $('#portfolio');
$container.isotope({
  itemSelector: '.col-sm-4',
  layoutMode: 'fitRows'
});
$('#filters').on( 'click', 'a', function() {
  var filterValue = $(this).attr('data-filter');
  $container.isotope({ filter: filterValue });   
  // change the lightbox group
  $(filterValue).each(function(i, obj) {
    if(obj.getElementsByTagName('a')[0] && 
       obj.getElementsByTagName('a')[0].dataset && 
      obj.getElementsByTagName('a')[0].dataset.lightbox){
        obj.getElementsByTagName('a')[0].dataset.lightbox=filterValue;
        console.log(obj.getElementsByTagName('a')[0].dataset.lightbox);   
    }
  });
  return false;
});
});